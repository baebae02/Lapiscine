int	ft_any(char **tab, int(*f)(char*))
{
	int	i;
	int	value;

	i = 0;
	value = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) != 0)
		{
			value = 1;
			break ;
		}
		i++;
	}
	return (value);
}
