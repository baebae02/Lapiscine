char	*ft_strlowcase(char *str)
{
	char	*idx;

	idx = str;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str += 32;
		}
		str++;
	}
	return (idx);
}
