#include <unistd.h>

void	ft_is_negative(int n)
{
	char	non;
	char	pass;

	non = 'N';
	pass = 'P';
	if (n < 0)
	{
		write(1, &non, 1);
	}
	else
	{
		write(1, &pass, 1);
	}
}
